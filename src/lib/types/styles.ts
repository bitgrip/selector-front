export enum ColorStyle {
	primary = 'primary', // Primary color
	primaryFocus = 'primary-focus', // Primary color when focused
	primaryContent = 'primary-content', // Foreground content color to use on primary color

	secondary = 'secondary', // Secondary color
	secondaryFocus = 'secondary-focus', // Secondary color when focused
	secondaryContent = 'secondary-content', // Foreground content color to use on secondary color

	accent = 'accent', // Accent color
	accentFocus = 'accent-focus', // Accent color when focused
	accentContent = 'accent-content', // Foreground content color to use on accent color

	neutral = 'neutral', // Neutral color
	neutralFocus = 'neutral-focus', // Neutral color when focused
	neutralContent = 'neutral-content', // Foreground content color to use on neutral color

	base100 = 'base-100', // Base color of page, used for blank backgrounds
	base200 = 'base-200', // Base color, a little darker
	base300 = 'base-300', // Base color, even more darker
	baseContent = 'base-content', // Base color, even more darker

	info = 'info', // Info color
	infoContent = 'info-content', // Foreground content color to use on info color

	success = 'success', // Success color
	successContent = 'success-content', // Foreground content color to use on success color

	warning = 'warning', // Warning color
	warningContent = 'warning-content', // Foreground content color to use on warning color

	error = 'error', // Error color
	errorContent = 'error-content' // Foreground content color to use on error color
}

